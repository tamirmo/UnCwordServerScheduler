
public class BusinessDataTO {
	
	long id;
	Integer time;
	
	public BusinessDataTO() {
		this.id = 3;
		this.time = 5;
	}
	
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}
		
	public Integer getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}
}
